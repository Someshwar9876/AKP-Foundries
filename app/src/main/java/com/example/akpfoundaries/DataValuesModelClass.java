package com.example.akpfoundaries;

public class DataValuesModelClass {

    int x;
    int y;

    public DataValuesModelClass(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public DataValuesModelClass(){

    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
